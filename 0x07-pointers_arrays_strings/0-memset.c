/**
 * _memset - a memory with a constant byte
 * @s: the memory to be filled
 * @b: char that have to be copied
 * @n: how many times is it to be copied
 * Return: pointer to the memory s
 */

char *_memset(char *s, char b, unsigned int n)
{
unsigned int m;

for (m = 0; i < m; i++)
{
s[i] = b;
}
return (s);
}
